#!/usr/bin/env python3

def multiply(input):
	print('Input: ' + str(input))

	output = str(2 * (input))
	
	print('Output: ' + output)

	return output


print('Returned: ' + multiply(1) + "\n")

print('Returned: ' + multiply(4) + "\n")

print('Returned: ' + multiply(10) + "\n")

print('Returned: ' + multiply(12))